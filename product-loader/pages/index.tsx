import ProductView from "@/components/productView"
import ProductLayout from "@/layouts/productLayout"
import IProduct from "@/types/Product"
import { GetServerSideProps, InferGetServerSidePropsType } from "next"
import Head from "next/head"
import { FC, useEffect } from "react"


const Catalog: FC<{ data: IProduct[] }> = ({ data }: InferGetServerSidePropsType<typeof getServerSideProps>) => {

  return (
    <>
      <Head>
        <title>Каталог</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
        <link
          rel="stylesheet"
          href="https://fonts.googleapis.com/icon?family=Material+Icons"
        />
      </Head>
      <ProductLayout title="Каталог">
        <ProductView willDeleteProduct={false} willLoadFunc={true} data={data} />
      </ProductLayout>
    </>

  )
}

export const getServerSideProps: GetServerSideProps<{ data: IProduct[] }> = async () => {
  try {
    const res = await fetch("https://dummyjson.com/products?limit=20");
    const json = await res.json();
    const ProductsApi = json.products;

    return {
      props: { data: ProductsApi }
    }
  } catch (err) {
    console.error(err);
    return {
      props: { data: [] } // возвращаем пустой массив, если возникла ошибка
    }
  }
}

export default Catalog

